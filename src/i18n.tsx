type I18nContextValue = {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

const formatVars = (text: string, vars?: Record<string, string | number>) => {
  if (!vars) return text;
  return Object.entries(vars).reduce(
    (acc, [key, value]) => acc.replace(new RegExp(`\\{${key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\}`, 'g'), String(value)),
    text
  );
};

export const LanguageProvider: React.FC<React.PropsWithChildren> = ({ children }) => {
  const initial = (localStorage.getItem(STORAGE_KEY) as Language) || 'en';
  const [lang, setLangState] = useState<Language>(initial);

  const setLang = useCallback((next: Language) => {
    setLangState(next);
    localStorage.setItem(STORAGE_KEY, next);
  }, []);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const table = translations[lang] || {};
      const translated = table[key] ?? key;
      return formatVars(translated, vars);
    },
    [lang]
  );

  const value = useMemo(() => ({ lang, setLang, t }), [lang, setLang, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    console.warn('useI18n must be used within LanguageProvider');
    return {
      lang: 'en' as Language,
      setLang: () => { },
      t: (key: string, vars?: Record<string, string | number>) => {
        const table = translations.en || {};
        const translated = table[key] ?? key;
        return formatVars(translated, vars);
      }
    };
  }
  return ctx;
};

export const translate = (key: string, vars?: Record<string, string | number>) => {
  const lang = (localStorage.getItem(STORAGE_KEY) as Language) || 'en';
  const table = translations[lang] || {};
  const translated = table[key] ?? key;
  return formatVars(translated, vars);
};
